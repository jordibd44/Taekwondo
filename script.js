    const ordenNiveles = ["blanco", "amarillo", "verde", "azul", "rojo", "dan1", "dan2", "dan3", "dan4", "dan5"];
    let nivelSeleccionado = null;
    let tecnicasDisponibles = [];
    let tecnicaActual = null;

    function seleccionarNivel(nivel) {
      nivelSeleccionado = nivel;

      if (nivel === "vocabulario") {
        // Solo usa las técnicas del vocabulario, sin mezcla
        tecnicasDisponibles = [...(tecnicasPorNivel["vocabulario"] || [])];
      } else {
        // Junta todas las técnicas hasta el nivel seleccionado
        const index = ordenNiveles.indexOf(nivel);
        tecnicasDisponibles = [];
        for (let i = 0; i <= index; i++) {
          tecnicasDisponibles = tecnicasDisponibles.concat(tecnicasPorNivel[ordenNiveles[i]] || []);
        }
      }

      document.getElementById('niveles').style.display = 'none';
      document.getElementById('contenido').style.display = 'flex';

      siguienteTecnica();
    }

    function siguienteTecnica() {
        if (tecnicasDisponibles.length === 0) {
            alert("¡Has completado todas las técnicas disponibles para este nivel!");
            reiniciar();
            return;
        }

        const random = Math.floor(Math.random() * tecnicasDisponibles.length);
        tecnicaActual = tecnicasDisponibles[random];

        // Eliminar técnica mostrada para evitar repetición
        tecnicasDisponibles.splice(random, 1);  

      document.getElementById('tecnicaNombre').innerText = tecnicaActual.nombre;
      document.getElementById('tecnicaImagen').innerHTML = "";
      document.getElementById('btnResolver').style.display = 'inline-block';

      if (nivelSeleccionado !== "vocabulario") {
        setTimeout(() => {
          mostrarImagen();
        }, 30000); // Espera 30 segundos antes de mostrar la imagen
      }

    }

    function mostrarImagen() {
      if (tecnicaActual) {
        if (nivelSeleccionado === "vocabulario") {
          document.getElementById('tecnicaImagen').innerHTML = `
            <p><strong>Traducción:</strong> ${tecnicaActual.imagen}</p>
          `;
        } else {
          document.getElementById('tecnicaImagen').innerHTML = `
            <img src="${tecnicaActual.imagen}" alt="${tecnicaActual.nombre}">
          `;
        }

        document.getElementById('btnResolver').style.display = 'none';
      }
    }

    function reiniciar() {
      document.getElementById('niveles').style.display = 'flex';
      document.getElementById('contenido').style.display = 'none';
      document.getElementById('modoEstudio').style.display = 'none';
      tecnicaActual = null;
      nivelSeleccionado = null;
      tecnicasDisponibles = [];
    }

function modoEstudiar() {
  // Oculta los otros menús
  document.getElementById('niveles').style.display = 'none';
  document.getElementById('contenido').style.display = 'none';
  document.getElementById('modoEstudio').style.display = 'flex';

  const tabla = document.getElementById('tablaEstudio');
  tabla.innerHTML = ''; // Limpiar

  // Mostrar técnicas por nivel
  for (const nivel of ordenNiveles) {
    if (!tecnicasPorNivel[nivel] || tecnicasPorNivel[nivel].length === 0) continue;

    const seccion = document.createElement('div');
    seccion.innerHTML = `<h3>${nivel.toUpperCase()}</h3>`;

    const grid = document.createElement('div');
    grid.className = 'tecnica-grid';

    tecnicasPorNivel[nivel].forEach(tecnica => {
      const card = document.createElement('div');
      card.className = 'tecnica-card';
      card.innerHTML = `
        <strong>${tecnica.nombre}</strong><br>
        <img src="${tecnica.imagen}" alt="${tecnica.nombre}">
      `;
      grid.appendChild(card);
    });

    seccion.appendChild(grid);
    tabla.appendChild(seccion);
  }

  // Mostrar vocabulario
  if (tecnicasPorNivel["vocabulario"] && tecnicasPorNivel["vocabulario"].length > 0) {
    const vocabSeccion = document.createElement('div');
    vocabSeccion.innerHTML = `<h3>VOCABULARIO</h3>`;

    const tablaVocabulario = document.createElement('table');
    tablaVocabulario.innerHTML = '<tr><th>Palabra</th><th>Traducción</th></tr>';

    tecnicasPorNivel["vocabulario"].forEach(entrada => {
      const fila = document.createElement('tr');
      fila.innerHTML = `<td>${entrada.nombre}</td><td>${entrada.imagen}</td>`; // imagen = traducción
      tablaVocabulario.appendChild(fila);
    });

    vocabSeccion.appendChild(tablaVocabulario);
    tabla.appendChild(vocabSeccion);
  }
}

